package myshop.model;

public class ImageVO {
	
	private int imgno;
	
	private String imgfilename;
	
	// 기본 생성자 생략되어 있다.

	public int getImgno() {
		return imgno;
	}

	public void setImgno(int imgno) {
		this.imgno = imgno;
	}

	public String getImgfilename() {
		return imgfilename;
	}

	public void setImgfilename(String imgfilename) {
		this.imgfilename = imgfilename;
	}
	
	

}
